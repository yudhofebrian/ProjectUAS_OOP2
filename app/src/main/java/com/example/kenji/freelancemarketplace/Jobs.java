package com.example.kenji.freelancemarketplace;

public class Jobs {
    String name;
    String phone;
    String email;
    String jobsname;
    String jobsdetail;
    String taken;
    public Jobs(String name, String phone, String email, String jobname, String jobdetail, String taken){
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.jobsname = jobname;
        this.jobsdetail = jobdetail;
        this.taken = taken;
    }
}
